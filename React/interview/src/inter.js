let number = 0;
console.log(number++); // console.log(number++): This uses the post-increment operator, which means it prints the current value of number (0) and then increments it by 1. So it prints 0 and then increments number to 1.

console.log(++number); // console.log(++number): This uses the pre-increment operator, which means it increments number by 1 first and then prints its value. Since number was incremented to 1 in the previous step, it will print 2.

console.log(number); // console.log(number): This simply prints the current value of number, which is now 2.
